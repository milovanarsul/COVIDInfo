import Foundation
import UIKit

func boldFont(size: CGFloat) -> UIFont{
    return UIFont(name: "IBMPlexSans-Bold", size: size)!
}

func regularFont(size: CGFloat) -> UIFont{
    return UIFont(name: "IBMPlexSans-Regular", size: size)!
}

func extraLightFont(size: CGFloat) -> UIFont{
    return UIFont(name: "IBMPlexSans-ExtraLight", size: size)!
}

func lightFont(size: CGFloat) -> UIFont{
    return UIFont(name: "IBMPlexSans-ExtraLight", size: size)!
}

func mediumFont(size: CGFloat) -> UIFont{
    return UIFont(name: "IBMPlexSans-Medium", size: size)!
}

func semiBoldFont(size: CGFloat) -> UIFont{
    return UIFont(name: "IBMPlexSans-SemiBold", size: size)!
}


